package day03_EscapeSequences;
/*
escape sequences: MUST be given with double qoute
\n: starts a new line
\t: paragraph space(tab)
\\: one back slash
\": double qoute
 */

public class EscapeSequences {

    public static void main(String[] args) {
        System.out.println("Java \nPython \nC#");

        System.out.println("-----------------------------------");

        System.out.println("Hello Cydeo \nHow are you all today? \nIt is nice to see you all! \nWhat class do we have next?");

        System.out.println("-----------------------------------");

        System.out.println("\tJava is Cool Programing Language");

        System.out.println("-----------------------------------");

        System.out.println("/ \\");  // in order to print one backward slash we need to give two backward slashes

        System.out.println("------------------------------------");

        System.out.println("My favorite TV show is \"Game Of Throne\"");  // in order to print double qoute,
    }



}
