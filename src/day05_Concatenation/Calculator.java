package day05_Concatenation;

public class Calculator {

    public static void main(String[] args) {

        int firtsNumber = 100;
        int secondNumber = 50;

        int addition = firtsNumber + secondNumber;
        int subtraction = firtsNumber - secondNumber;
        int multiplication = firtsNumber * secondNumber;

        // 100 + 50 = 150
        System.out.println(firtsNumber + " + " + secondNumber +" = " + addition);

        // 100 - 50 = 50
        System.out.println(firtsNumber + " - "+ secondNumber+ " = " + subtraction);

        // 100 * 50 = 5000
        System.out.println(firtsNumber + " * " + secondNumber + " = " + multiplication);




    }


}
