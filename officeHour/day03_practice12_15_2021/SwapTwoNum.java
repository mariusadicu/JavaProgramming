package day03_practice12_15_2021;

public class SwapTwoNum {

    public static void main(String[] args) {

        int a = 10;
        int b = 15;

        // swap ==> a = 15 , b = 10

        int temp = a;

        a = b; // a = 15
        //   b = a; // b = 15
        b = temp; // b = 10

        System.out.println(a);
        System.out.println(b);
















    }
}
